        bot.reply_to(message, answer)
        
        # –õ–æ–≥–∏—Ä—É–µ–º –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
        print(f"‚úÖ –ó–∞–ø—Ä–æ—Å: {message.text}")
        print(f"‚úÖ –û—Ç–≤–µ—Ç: {answer}")
        
